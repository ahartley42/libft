#include "libft.h"

char	*ft_strnew(size_t size)
{
	int		i;
	char	*ans;

	i = 0;
	ans = (char *)malloc(size);
	while (size > 0)
	{
		ans[i] = '\0';
		i++;
		size--;
	}
	return (ans);
}

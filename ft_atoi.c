int	ft_atoi(const char *str)
{
	int i;
	int ans;

	i = 0;
	ans = 0;
	if ((str[i] == '+') || (str[i] == '-'))
		i++;
	while ((str[i] >= '0') && (str[i] <= '9'))
	{
		ans = ans * 10 + str[i] - '0';
		i++;
	}
	if (str[0] == '-')
		ans = ans * -1;
	return (ans);
}
